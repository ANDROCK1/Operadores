// Igualdad (==)
const ResultadoIgualdad = 10  == 10;
console.log(ResultadoIgualdad); // true

// Desigualdad  (!=)
const ResultadoDesigualdad = 15 !== "15";
console.log(ResultadoDesigualdad); // true

//  Mayor que (>)
const ResultadoMayorQue = 20 > 10;
console.log(ResultadoMayorQue); // true

//   Menor que (<)
const ResultadoMenorQue = 8 < 12;
console.log(ResultadoMenorQue); // true

//   Mayor o igual que (>=)
const ResultadoMayorOIGualQue = 22 >= 20;
console.log(ResultadoMayorOIGualQue); // true

//    Menor o igual que (<=)
const ResultadoMenorOIGualQue = 15 <= 15;
console.log(ResultadoMenorOIGualQue); // true

